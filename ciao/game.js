let state = {}

function startGame() {
  state = {}
  showTextNode(1)
}

function showTextNode(textNodeIndex) {
  const textNode = textNodes.find(textNode => textNode.id === textNodeIndex)
  textElement.innerText = textNode.text
  imgElement.src = textNode.image
  while (optionButtonsElement.firstChild) {
    optionButtonsElement.removeChild(optionButtonsElement.firstChild)
  }

  if (document.getElementById('image').src.includes('faces/playlist.gif')){
    document.getElementById('image').parentElement.href = 'https://open.spotify.com/playlist/6vruz3Ehaho9uOuUzo5Umj?si=eb803de6e1c54fe7';
    document.getElementById('image').parentElement.onclick = function() { };
  } else if (document.getElementById('image').src.includes('faces/film.gif')){
    document.getElementById('image').parentElement.removeAttribute('href');
    document.getElementById('image').parentElement.onclick = function() { alert(filmList); };
  } else if (document.getElementById('image').src.includes('faces/famiglia.gif')){
    document.getElementById('image').parentElement.removeAttribute('href');
    document.getElementById('image').parentElement.onclick = function() { alert(storiaFamiglia); };
  } else if (document.getElementById('image').src.includes('faces/github.gif')){
    document.getElementById('image').parentElement.href = 'https://github.com/schlatterca/schlatterca.com';
    document.getElementById('image').parentElement.onclick = function() { };
  } else {
    document.getElementById('image').parentElement.removeAttribute('href');
    document.getElementById('image').parentElement.onclick = function() { };
  }

  textNode.options.forEach(option => {
    if (showOption(option)) {
      if ((option.text == 'Combatti!')||(option.text == 'Voglio combattere!')) {
        const button = document.createElement('a')
        button.href = 'lotta/index.html'
        button.target = '_blank'
        button.innerText = option.text
        button.classList.add('btn')
        button.onclick = function() { alert('Complimenti! Hai sbloccato il simulatore di botte!'); document.getElementById('fight_button').classList.remove('displayNone'); };     
        //button.addEventListener('click', () => selectOption(option))
        optionButtonsElement.appendChild(button)
      } else if ((option.text == 'Voglio giocare!')||(option.text == 'Voglio correre!')) {
        const button = document.createElement('a')
        button.href = 'corsa/index.html'
        button.target = '_blank'
        button.innerText = option.text
        button.classList.add('btn')
        button.onclick = function() { alert('Complimenti! Hai sbloccato il simulatore di corsa!'); document.getElementById('run_button').classList.remove('displayNone'); };
        //button.addEventListener('click', () => selectOption(option))
        optionButtonsElement.appendChild(button)
      } else {
        const button = document.createElement('button')
        button.innerText = option.text
        button.classList.add('btn')
        button.addEventListener('click', () => selectOption(option))
        optionButtonsElement.appendChild(button)
      } 
    }
  })
}

function showOption(option) {
  return option.requiredState == null || option.requiredState(state)
}

function selectOption(option) {
  const nextTextNodeId = option.nextText

  if (option.setState) {
    console.log(option.setState);
    if (option.setState.pace == true){
      window.alert('Hai deciso di fare la pace!\n…Non la scelta migliore, se posso permettermi.');
    }
  }


  if (nextTextNodeId <= 0) {
    return startGame()
  }

  state = Object.assign(state, option.setState)
  showTextNode(nextTextNodeId)
}

